    /**
     * Method that finds a space on the board based on id.
     * @param id id-number of the space
     * @return The space if id exists, else null
     */
    Space findSpace(int id)
    {
        for (Space space : spaces) {
            if (space.getId() == id) {
                return space;
            }
        }
        return null;
    }
